/**
 * setup-test-key.js
 *
 * Creates test API key fixtures in the database for the integration test suite.
 *
 * This script is strictly dev/test tooling. It NEVER runs in production.
 * Do not import this file from any application code.
 *
 * Usage:
 *   node setup-test-key.js
 *
 * Required env vars (from your .env.local or exported in the shell):
 *   NEXT_PUBLIC_SUPABASE_URL
 *   SUPABASE_SERVICE_ROLE_KEY
 *
 * Outputs a .env.test file that test-api.js reads at runtime.
 * That file is git-ignored — never commit it.
 */

'use strict';

require('dotenv').config({ path: '.env.local' });

console.log('SUPABASE URL:', process.env.NEXT_PUBLIC_SUPABASE_URL);
console.log('SERVICE KEY EXISTS:', !!process.env.SUPABASE_SERVICE_ROLE_KEY);

const { createHash, randomBytes } = require('crypto');
const { createClient } = require('@supabase/supabase-js');
const fs = require('fs');
const path = require('path');

// ─── Env validation ───────────────────────────────────────────────────────────

const supabaseUrl =
  process.env.NEXT_PUBLIC_SUPABASE_URL || process.env.VITE_SUPABASE_URL;
const supabaseKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!supabaseUrl) {
  console.error('✗ Missing env var: NEXT_PUBLIC_SUPABASE_URL');
  process.exit(1);
}
if (!supabaseKey) {
  console.error('✗ Missing env var: SUPABASE_SERVICE_ROLE_KEY');
  console.error('  Run: SUPABASE_SERVICE_ROLE_KEY=your_key node setup-test-key.js');
  process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseKey);

// ─── Helpers ──────────────────────────────────────────────────────────────────

function hashApiKey(key) {
  return createHash('sha256').update(key).digest('hex');
}

/** Generates a key using the same algorithm as production hash.ts */
function generateTestKey(label) {
  return `txcat_test_${label}_${randomBytes(12).toString('hex')}`;
}

async function upsertKey({ name, key, isActive, monthlyLimit }) {
  const keyHash = hashApiKey(key);

  const { data, error } = await supabase
    .from('api_keys')
    .upsert(
      {
        name,
        key_hash: keyHash,
        is_active: isActive,
        monthly_limit: monthlyLimit,
      },
      { onConflict: 'key_hash' }
    )
    .select('id, name, is_active, monthly_limit')
    .single();

  if (error) {
    throw new Error(`Failed to upsert key "${name}": ${error.message}`);
  }

  return data;
}

// ─── Main ─────────────────────────────────────────────────────────────────────

async function main() {
  console.log('Setting up test fixtures...\n');

  // Active key — used by most tests
  const activeKey = generateTestKey('active');
  const activeRecord = await upsertKey({
    name: '[TEST] Active key',
    key: activeKey,
    isActive: true,
    monthlyLimit: 10000,
  });
  console.log(`✓ Active key created   id=${activeRecord.id}`);

  // Inactive key — used by the inactive-key rejection test
  const inactiveKey = generateTestKey('inactive');
  await upsertKey({
    name: '[TEST] Inactive key',
    key: inactiveKey,
    isActive: false,
    monthlyLimit: 10000,
  });
  console.log('✓ Inactive key created');

  // Quota-exhausted key — monthly_limit set to 0 so every request exceeds it
  const exhaustedKey = generateTestKey('exhausted');
  await upsertKey({
    name: '[TEST] Exhausted quota key',
    key: exhaustedKey,
    isActive: true,
    monthlyLimit: 0,
  });
  console.log('✓ Exhausted-quota key created');

  // Write a .env.test file so test-api.js can read the generated keys without
  // them being baked into source control.
  const envPath = path.resolve(__dirname, '.env.test');
  const envContent = [
    '# Auto-generated by setup-test-key.js — do not commit',
    `TEST_KEY_ACTIVE=${activeKey}`,
    `TEST_KEY_INACTIVE=${inactiveKey}`,
    `TEST_KEY_EXHAUSTED=${exhaustedKey}`,
    '',
  ].join('\n');

  fs.writeFileSync(envPath, envContent, 'utf8');
  console.log(`\n✓ Keys written to ${envPath}`);
  console.log('  (This file is git-ignored — never commit it)\n');
}

main().catch((err) => {
  console.error(`\n✗ Setup failed: ${err.message}`);
  process.exit(1);
});
